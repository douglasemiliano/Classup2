package com.ifpb.classup.model;

public class Estatisticas {
    private int totalBadges;
    private int totalAtividadesConcluidas;
    private double pontosTotais;

    public int getTotalBadges() {
        return totalBadges;
    }

    public void setTotalBadges(int totalBadges) {
        this.totalBadges = totalBadges;
    }

    public int getTotalAtividadesConcluidas() {
        return totalAtividadesConcluidas;
    }

    public void setTotalAtividadesConcluidas(int totalAtividadesConcluidas) {
        this.totalAtividadesConcluidas = totalAtividadesConcluidas;
    }

    public double getPontosTotais() {
        return pontosTotais;
    }

    public void setPontosTotais(double pontosTotais) {
        this.pontosTotais = pontosTotais;
    }
}

